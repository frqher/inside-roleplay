propertyID = {}
for k,v in pairs ( handlingLimits ) do
    propertyID[v.id] = k
end

function getHandlingPropertyNameFromID ( id )
    id = tonumber ( id )
    if not id then
        return false
    end
    return propertyID[id]
end

function stringToValue ( property, value )
    if property == "ABS" then
        return tobool ( value )
    end

    if property == "modelFlags" or property == "handlingFlags" then
        return tonumber ( "0x"..value )
    end

    if property == "driveType" then
        if value == "r" then return "rwd" end
        if value == "f" then return "fwd" end
        if value == "4" then return "awd" end
        return value

    elseif property == "engineType" then
        if value == "p" then return "petrol" end
        if value == "d" then return "diesel" end
        if value == "e" then return "electric" end
        return value
    end

    return tonumber ( value ) or value
end

function tobool ( var )
    if type(var) == "nil" then return nil end
    local conform = {
        [0]=false, [1] = true,
        ["0"]=false, ["1"] = true,
        ["false"] = false, ["true"] = true,
        [true] = true, [false] = false,
    }
    local t = type ( var )
    if t == "number" or t == "string" or t == "boolean" then
        if conform[var] == nil then
            error ( "tobool'da dönüştürülecek geçersiz dize veya sayı! [arg:1,"..tostring(var).."]", 2 )
        end
        return conform[var]
    end
    error ( "tobool'da dönüştürülecek geçersiz değer! [arg:1,"..tostring(var).."]", 2 )
    return nil
end