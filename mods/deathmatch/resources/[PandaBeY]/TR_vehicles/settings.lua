function getVehicleMaxSpeed(model, engineCapacity)
	local speedAdd = (engineCapacity - 1.9) * 10 * (handlingVehicles[model][2] or 5)
	if categoryA[model] then speedAdd = (engineCapacity - 0.8) * 10 * (handlingVehicles[model][2] or 5) end
	return handlingVehicles[model][1] + speedAdd
end
