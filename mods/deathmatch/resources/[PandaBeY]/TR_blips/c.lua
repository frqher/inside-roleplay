function startScript()
    createBlips()
    createATMBlips()

    createMarkers()
end



function createBlips()
    for i, v in pairs(blips) do
        local pos = v.pos
        local color = v.color
        local blip = createBlip(pos.x, pos.y, pos.z, 0, 0, color[1], color[2], color[3], 255, 1)
        setElementData(blip, "icon", v.blip, false)
        if v.blipName then setElementData(blip, "blipName", v.blipName, false) end
    end
end

function createATMBlips()
    for i, v in pairs(getElementsByType("object")) do
        if getElementModel(v) == 2943 then
            local blip = createBlip(Vector3(getElementPosition(v)), 0, 2, 43, 143, 46, 255, 0)
            setElementData(blip, "icon", 23, false)
            getElementModel(v)
        end
    end
end

function createMarkers()
    for i, v in pairs(markers) do
        local marker = createMarker(v.pos.x, v.pos.y, v.pos.z - 1, "cylinder", 1.2, v.color[1], v.color[2], v.color[3], 0)
        setElementInterior(marker, v.int)
        setElementDimension(marker, v.dim)
        setElementData(marker, "markerIcon", v.icon, false)
    end
end

startScript()